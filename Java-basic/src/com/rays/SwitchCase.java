package com.rays;

public class SwitchCase {
	public static void main(String[] args) {
		String a="Patel";
		switch(a) {
		case "Shruti":
			System.out.println("Name");
			break;
		case "Patel":
			System.out.println("Surname");
			break;
		case "Khargone":
			System.out.println("City");
			break;
			default:
				System.out.println("Nothing else");
				break;
		}
				
	}
   
}
